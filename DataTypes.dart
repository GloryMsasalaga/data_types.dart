void main() {
  //Integers: used to represent whole numbers
  int age = 25;

  //Doubles: used to represent floating-point numbers
  double weight = 75.5;

  //Strings: used to represent textual data
  String name = 'Glory Lazaro';

  //List: used to represent an ordered collection of objects
  List<int> numbers = [1, 2, 3, 4, 5];

  //Maps: used to represent a collection of key-value pairs
  Map<String, String> person = {
    'name': 'Glory',
    'age': '23',
    'job': 'Geomatician and Computer Networker'
  };

  //Printing out the values to demonstrate their use
  print('Age: $age');
  print('Weight: $weight');
  print('Name: $name');
  print('Numbers: $numbers');
  print('Person: $person');
}
